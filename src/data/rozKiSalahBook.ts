// Programmatic sequential calendar generator for Day 1 - Day 365
const monthsOrder = [
  "January", "February", "March", "April", "May", "June", 
  "July", "August", "September", "October", "November", "December"
];

const buildCalendarBook = (): Record<number, QuoteNode> => {
  const book: Record<number, QuoteNode> = {};
  let dayCounter = 1;

  for (const monthName of monthsOrder) {
    const list = MONTHLY_RECORDS[monthName];
    // Jan has 31 days, Feb has 28 days (or 29 max), Mar has 31, Apr 30, May 31, Jun 30, Jul 31, Aug 31, Sep 30, Oct 31, Nov 30, Dec 31
    let daysInMonth = 31;
    if (monthName === "February") daysInMonth = 28;
    else if (["April", "June", "September", "November"].includes(monthName)) daysInMonth = 30;

    for (let d = 1; d <= daysInMonth; d++) {
      const quoteIndex = (d - 1) % list.length;
      book[dayCounter] = {
        text: list[quoteIndex],
        author: "युगप्रधान आचार्य श्री महाश्रमण जी",
        month: monthName
      };
      dayCounter++;
    }
  }

  // Handle remaining days if any leap/index adjustments
  for (let i = dayCounter; i <= 366; i++) {
    book[i] = {
      text: MONTHLY_RECORDS["December"][0],
      author: "युगप्रधान आचार्य श्री महाश्रमण जी",
      month: "December"
    };
  }

  return book;
};

export const rozKiSalahBook: Record<number, QuoteNode> = buildCalendarBook();
